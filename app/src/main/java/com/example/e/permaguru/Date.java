public class Date {

    int day;
    int month;
    int year;
    String name;
    String description;
    int id;


    Date(int day, int month, int year, String name, String description, int id) {
        this.day = day;
        this.month = month;
        this.year = year;
        this.name = name;
        this.description = description;
        this.id = id;
    }

}
